1. 트랜잭션이란?
데이터베이스에서 하나의 논리적인 작업 단위를 의미하며, 데이터의 무결성과 일관성을 보장하기 위해 사용됩니다.
ACID라고 부르는 4가지 특성이 있습니다.
A - 원자성 (Atomicity) : 모든 작업이 전부 수행되거나, 전혀 수행되지 않아야 함. 중간 실패 시 Rollback으로 원상복구.
C - 일관성 (Consistency) : 트랜잭션 전후의 데이터가 일관된 상태를 유지해야 함. 무결성 제약조건 유지.
I - 격리성 (Isolation) : 여러 트랜잭션이 동시에 실행되어도, 서로 간섭하지 않도록 독립적으로 실행되어야 함.
D - 지속성 (Durability) : Commit된 데이터는 시스템 장애가 발생해도 영구적으로 저장됨.

2. 기본키란?
테이블에서 각 행(row)을 고유하게 식별하는 데 사용되는 열(column) 또는 열들의 조합입니다.
데이터베이스 설계의 핵심 요소로, 데이터 무결성을 유지하는 데 중요한 역할을 합니다.

특징으로는 
유일성 (Unique) : 기본키로 지정된 열의 값은 모든 행에서 고유해야 함
NOT NULL : 기본키는 NULL 값을 허용하지 않음 (필수값이어야 함)
한 테이블에 하나만 존재 가능 : 기본키는 테이블당 단 하나만 지정할 수 있음
기본적으로 인덱스가 생성됨 : 기본키를 설정하면 해당 열에 자동으로 인덱스가 생성되어 성능 향상
을 들수 있습니다.